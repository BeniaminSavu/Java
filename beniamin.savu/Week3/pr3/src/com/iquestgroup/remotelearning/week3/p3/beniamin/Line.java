package com.iquestgroup.remotelearning.week3.p3.beniamin;

public class Line implements Shape{
	private Point pointA;
	private Point pointB;
	
	Line(Point pointA, Point pointB){
		this.pointA = pointA;
		this.pointB = pointB;
	}

	@Override
	public void draw() {
		System.out.println("--Drawing Line--");
		System.out.println("Drawing first point at " + pointA);
		System.out.println("Drawing second point at " + pointB);
		System.out.println("Drawing a line between the 2 points");
	}
}
